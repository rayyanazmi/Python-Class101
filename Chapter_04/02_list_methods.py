l1.sort()
print(l1)

l1 = [1, 34, 64, 5, 6, 11]
l1.reverse()
print(l1)

l1 = [1, 34, 64, 5, 6, 11]
l1.insert(3, 65) # Inserting 65 such that it is index 3
print(l1)

l1 = [1, 34, 64, 5, 6, 11]
